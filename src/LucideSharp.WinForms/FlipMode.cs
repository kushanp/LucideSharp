namespace LucideSharp.WinForms;

/// <summary>
/// Specifies how an icon should be flipped.
/// </summary>
public enum FlipMode
{
    /// <summary>No flip.</summary>
    None = 0,

    /// <summary>Flip horizontally.</summary>
    Horizontal = 1,

    /// <summary>Flip vertically.</summary>
    Vertical = 2,

    /// <summary>Flip both horizontally and vertically.</summary>
    Both = 3
}